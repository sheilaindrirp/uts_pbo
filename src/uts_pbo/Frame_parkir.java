private void selectData() {
        String kolom[] = {"No_Resi", "Nama", "Nopol", "Jenis_Kendaraan", "Tangga_lMasuk", "Tangga_lKeluar", "Total"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_parkir";
        ResultSet rs = koneksiDB.executeQuery(SQL);
        try {
            while (rs.next()) {
                String NoResi = rs.getString(1);
                String NOPOL = rs.getString(3);
                String JenisKendaraan = rs.getString(4);
                String TanggalMasuk = rs.getString(5);
                String TanggalKeluar = rs.getString(6);
                String Harga = rs.getString(7);
                String NamaPetugas = rs.getString(2);
                String data[] = {NoResi, NOPOL, JenisKendaraan, TanggalMasuk, TanggalKeluar, Harga, NamaPetugas};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Frame_parkir.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel.setModel(dtm);
    }
