    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String Masuk = dateFormat.format(tglmasuk.getDate());
        String Keluar = dateFormat.format(tglkeluar.getDate());
         int harga = 3000;
         
        harimasukint = Integer.parseInt(Masuk);
        harikeluarint = Integer.parseInt(Keluar);
        
        totalhari = harimasukint - harikeluarint;
        hargatotal = totalhari * harga;
        String hargafinal = Integer.toString(hargatotal);

        String combobox = jk.getSelectedItem().toString();

         if ("".equals(nopol.getText()) || "".equals(nama.getText()) 
                || "".equals(tglkeluar.equals("")) || "".equals(tglmasuk.equals(""))) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {

           String SQL = "INSERT INTO tb_parkir (No_resi,Nama,Nopol,Jenis_Kendaraan,Tanggal_Masuk,Tanggal_Keluar,Total)"
                    + "VALUES"
                    + "('"+nama.getText()+"',"
                    + "'"+nopol.getText()+"',"
                    + "'"+jk+"',"
                    + "'" 
                    + combobox 
                    + "','" + tglmasuk.equals("") 
                    + "','" + tglkeluar.equals("") 
                    + "','" + hargafinal + "')";
            int status = koneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton4ActionPerformed
        // TODO add your handling code here:
        int baris = tabel.getSelectedRow();
        if (baris != -1) {
            String Nopol = tabel.getValueAt(baris, 1).toString();
            String SQL = "SELECT * FROM tb_parkir WHERE Nopol = '" + Nopol + "'";
            ResultSet rs = koneksiDB.executeQuery(SQL);
            try {
                while (rs.next()) {
                    id = rs.getString(1);
                    namapetugas = rs.getString(2);
                    jenis_kendaraan = rs.getString(4);
                    tanggal_masuk = rs.getString(5);
                    tanggal_keluar = rs.getString(6);
                    total = rs.getString(7);
                    int totalhariint,tglmsk,tglklr;
                    tglmsk = Integer.parseInt(tanggal_masuk);
                    tglklr = Integer.parseInt(tanggal_keluar);
                    totalhariint = tglklr-tglmsk;
                    totalharistr = Integer.toString(totalhariint);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Frame_parkir.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showConfirmDialog(null, "No Resi : " + id + "\n" + "Nama Petugas : " + namapetugas + "\n" + "Nomor Polisi : " + nopol + "\n" + "Jenis Kendaraan : " + jenis_kendaraan + "\n" + "Tanggal Masuk : " + tanggal_masuk + "\n" + "Tanggal Keluar : " + tanggal_keluar + "\n" + "Total Hari : " + totalharistr + "\n" + "Total Harga : " + total + "\n", "Rekapan Parkir", JOptionPane.DEFAULT_OPTION);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Baris Untuk Dilihat :)", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton4ActionPerformed

